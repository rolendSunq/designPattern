package command.part4;

public class GarageDoor {

	public GarageDoor() {
	}

	public void up() {
		System.out.println("차고문이 열렸습니다.");
	}

	public void down() {
		System.out.println("차고문이 닫혔습니다.");
	}

	public void stop() {
		System.out.println("차고문이 멈췄습니다.");
	}

	public void lightOn() {
		System.out.println("차고의 조명을 켰습니다.");
	}

	public void lightOff() {
		System.out.println("차고의 조명을 껏습니다.");
	}
}
